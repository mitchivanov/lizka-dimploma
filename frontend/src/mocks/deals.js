// Функции для фильтрации
export const getDealsByType = (type) => {
  return deals.filter(deal => deal.deal_type_name.toLowerCase() === type.toLowerCase());
};

export const getDealsByRegion = (regionName) => {
  return deals.filter(deal => deal.region_name === regionName);
};

export const getDealsByBranch = (branchName) => {
  return deals.filter(deal => deal.deal_branch_name === branchName);
};

export const getDealsInPriceRange = (minPrice, maxPrice) => {
  return deals.filter(deal => deal.seller_price >= minPrice && deal.seller_price <= maxPrice);
}; 